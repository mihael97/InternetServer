package hr.fer.zemris.java.graphics.objects;

public class BoundingRect {
	int left;
	int top;
	int right;
	int bottom;

	public BoundingRect(int left, int top, int right, int bottom) {
		super();
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
